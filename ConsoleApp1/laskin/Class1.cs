using System;
using System.Collections.Generic;
using System.Text;

namespace laskin
{
    static class laskin
    {
        public static float summa(float a, float b)
        {
            return a + b;

        }

        public static float erotus(float a, float b)
        {
            return a - b;

        }
        public static float kerto(float a, float b)
        {
            return a * b;

        }

        public static float jako(float a, float b)
        {
            return a / b;

        }
    }
}